

public class calculadora {
		
		public static int suma (int a, int b) {
			return a+b;
		}
		
		public int multiplica(int a, int b) {
			return a*b;
		}
		
		public int division(int a,int b) {
			return a/b;
		}
		
		public int resta (int a, int b) {
			return a-b;
		}
		
		public int resto (int a, int b) {
			return a%b;
		}
		
	}

